let currentLang = localStorage.getItem('catharei-lang') || 'en';
let cartItems = JSON.parse(localStorage.getItem('catharei-cart-data') || '[]');
// Fallback array for empty cart scenario

document.addEventListener('DOMContentLoaded', async () => {
  initLanguage();
  updateCartBadgeUI();
  renderCartDropdown(); // Initially render the hover dropdown if it exists
  
  // Render based on what page we are on
  const grid = document.getElementById('product-grid');
  if(grid) {
    const category = grid.getAttribute('data-category');
    const isFeatured = grid.getAttribute('data-type') === 'featured';

    try {
      let url = '/api/products';
      if (category) url += `?category=${encodeURIComponent(category)}`;

      const response = await fetch(url);
      const products = await response.json();

      if (isFeatured) {
        renderProducts(products.filter(p => p.featured === 1).sort((a, b) => a.name.localeCompare(b.name)));
      } else {
        renderProducts(products.sort((a, b) => a.name.localeCompare(b.name)));
      }
    } catch (err) {
      console.error("Failed to load products:", err);
      grid.innerHTML = `<p style='text-align:center;width:100%;grid-column:1/-1;color:#999;'>Server offline. Please run <code>node server.js</code></p>`;
    }
  }

  setupEventListeners();
  applyTranslations();
  setupFAQ();
  syncCategoryCircleImages();
  initCakeAnimation();
  checkLoginStatus();


  // If on the cart.html page, render the cart list
  if(document.getElementById('cart-page-body')) {
    renderCartPage();
  }
});

function initLanguage() {
  const htmlTag = document.documentElement;
  htmlTag.setAttribute('dir', currentLang === 'ar' ? 'rtl' : 'ltr');
  htmlTag.setAttribute('lang', currentLang);
}

function updateCartBadgeUI() {
  const badge = document.getElementById('cart-badge');
  if(badge) {
    let count = 0;
    cartItems.forEach(item => count += item.qty);
    badge.textContent = count;
  }
}

function setupEventListeners() {
  // Mobile Menu Toggle
  const menuToggle = document.getElementById('mobile-menu-toggle');
  const navList = document.getElementById('nav-list');
  if(menuToggle && navList) {
    menuToggle.addEventListener('click', () => {
      navList.classList.toggle('active');
    });
  }

  // Search Toggle
  const searchBtn = document.getElementById('search-btn');
  const searchContainer = document.getElementById('search-container');
  const searchInput = document.getElementById('search-input');
  if(searchBtn) {
    searchBtn.addEventListener('click', () => {
      searchContainer.classList.toggle('active');
      if(searchContainer.classList.contains('active')) {
        searchInput.focus();
      }
    });
  }

  // Cart Button Redirection
  const cartBtn = document.getElementById('cart-btn');
  if(cartBtn) {
    cartBtn.addEventListener('click', () => {
      let cartUrl = window.location.pathname.includes('/navigation/') ? 'cart.html' : 'navigation/cart.html';
      // Do not redirect if we are already on the cart page (pathname ends with cart.html)
      if(!window.location.pathname.endsWith('cart.html')) {
         window.location.href = cartUrl;
      }
    });
  }

  // Account Button Redirection
  const accountBtn = document.getElementById('account-btn');
  if(accountBtn) {
    accountBtn.addEventListener('click', () => {
      handleAccountClick();
    });
  }

  // Dropdowns for Mobile
  const dropdownParents = document.querySelectorAll('.has-dropdown');
  dropdownParents.forEach(parent => {
    parent.addEventListener('click', (e) => {
      if(window.innerWidth <= 768) {
        if(e.target.tagName === 'BUTTON' || e.target.parentElement.tagName === 'BUTTON') {
          // close others
          dropdownParents.forEach(other => {
            if(other !== parent) other.classList.remove('open');
          });
          parent.classList.toggle('open');
        }
      }
    });
  });
}

async function syncCategoryCircleImages() {
  const circles = document.querySelectorAll('.category-circle-item');
  if (circles.length === 0) return;

  try {
    const res = await fetch('/api/products');
    const products = await res.json();

    circles.forEach(circle => {
      const categoryId = circle.getAttribute('data-category');
      if (!categoryId) return;

      const img = circle.querySelector('.category-img');
      if (!img) return;

      // Find the first product in this category
      const product = products.find(p => p.category === categoryId);
      if (product && product.image) {
        let imgSrc = product.image;
        if (!imgSrc.startsWith('http') && !imgSrc.startsWith('/') && !imgSrc.startsWith('data:')) {
          imgSrc = '/' + imgSrc;
        }
        img.src = imgSrc;
      }
    });
  } catch (err) {
    console.error("Failed to sync category images:", err);
  }
}

function setupFAQ() {
  const faqItems = document.querySelectorAll('.faq-item');
  faqItems.forEach(item => {
    const question = item.querySelector('.faq-question');
    if(question) {
      question.addEventListener('click', () => {
        // close others (optional, but requested accordion style often implements this)
        faqItems.forEach(otherItem => {
          if(otherItem !== item) otherItem.classList.remove('active');
        });
        item.classList.toggle('active');
      });
    }
  });
}

function renderProducts(productsList) {
  const grid = document.getElementById('product-grid');
  if(!grid) return;
  grid.innerHTML = ''; // Clear

  if(productsList.length === 0) {
    grid.innerHTML = `<div style='grid-column:1/-1;text-align:center;padding:60px 20px;color:#666;'>
      <svg width="48" height="48" viewBox="0 0 24 24" fill="#333" style="margin-bottom:16px;display:block;margin-left:auto;margin-right:auto;"><path d="M11 9H9V2H7v7H5V2H3v7c0 2.12 1.66 3.84 3.75 3.97V22h2.5v-9.03C11.34 12.84 13 11.12 13 9V2h-2v7zm5-3v8h2.5v8H21V2c-2.76 0-5 2.24-5 4z"/></svg>
      <p style="font-family:'Playfair Display',serif;font-size:1.2rem;color:#D4AF37;margin-bottom:8px;">No products found</p>
    </div>`;
    return;
  }

  productsList.forEach(product => {
    const card = document.createElement('div');
    card.className = 'product-card';
    
    // Dynamic naming handling for database (translates if nameKey exists, otherwise uses freeform name)
    let titleText = product.name;
    let titleI18nAttr = '';
    
    if (product.nameKey && translations[currentLang][product.nameKey]) {
        titleText = translations[currentLang][product.nameKey];
        titleI18nAttr = `data-i18n="${product.nameKey}"`;
    }

    // Resolve image path: absolute URLs pass through; relative paths get a leading /
    let imgSrc = product.image || '';
    if (imgSrc && !imgSrc.startsWith('http') && !imgSrc.startsWith('/') && !imgSrc.startsWith('data:')) {
      imgSrc = '/' + imgSrc;
    }

    card.innerHTML = `
      <img src="${imgSrc}" alt="${titleText}" class="product-image" onerror="this.style.background='#1e1e1e'">
      <div class="product-info">
        <div>
          <h3 class="product-title" ${titleI18nAttr}>${titleText}</h3>
          <div class="product-description" style="font-size: 0.85rem; color: var(--color-text-secondary); margin-bottom: 10px;">
            ${product.description || ''}
          </div>
          <div class="product-price">Qr ${product.price}</div>
        </div>
        <button class="btn btn-primary add-to-cart" onclick="addToCart(event, ${product.id || Date.now()}, '${titleText.replace(/'/g, "\\'")}', ${product.price}, '${imgSrc.replace(/'/g, "\\'")}')" data-i18n="btn.addCart">${translations[currentLang]["btn.addCart"]}</button>
      </div>
    `;
    grid.appendChild(card);
  });
}

function addToCart(e, id, name, price, image) {
  if(e) e.preventDefault();
  
  // Check if item exists in cart already
  let existingItem = cartItems.find(item => item.id === id);
  if(existingItem) {
    existingItem.qty += 1;
  } else {
    cartItems.push({ id, name, price, image, qty: 1 });
  }

  // Save to persistent storage
  localStorage.setItem('catharei-cart-data', JSON.stringify(cartItems));
  
  // Update UI Elements
  updateCartBadgeUI();
  const badge = document.getElementById('cart-badge');
  if(badge) {
    badge.classList.remove('pop');
    void badge.offsetWidth; 
    badge.classList.add('pop');
  }

  showToast(translations[currentLang]["toast.added"] || "Item added to cart");
  renderCartDropdown(); // Refresh the DOM elements on the dropdown
}

function removeFromCart(id) {
  cartItems = cartItems.filter(item => item.id !== id);
  localStorage.setItem('catharei-cart-data', JSON.stringify(cartItems));
  updateCartBadgeUI();
  renderCartDropdown();
  if(document.getElementById('cart-page-body')) {
    renderCartPage();
  }
}

function updateCartQuantity(id, delta) {
  let existingItem = cartItems.find(item => item.id === id);
  if(existingItem) {
    existingItem.qty += delta;
    if(existingItem.qty <= 0) {
      removeFromCart(id);
      return;
    }
  }
  localStorage.setItem('catharei-cart-data', JSON.stringify(cartItems));
  updateCartBadgeUI();
  renderCartDropdown();
  if(document.getElementById('cart-page-body')) {
    renderCartPage();
  }
}

function renderCartDropdown() {
  const dropdown = document.getElementById('cart-dropdown');
  if(!dropdown) return;
  
  if(cartItems.length === 0) {
    dropdown.innerHTML = '<div style="padding:15px;text-align:center;color:#999;font-size:0.9rem;">Your cart is empty</div>';
    return;
  }
  
  let html = '<div class="cart-dropdown-list">';
  let total = 0;
  cartItems.forEach(item => {
    let sub = item.price * item.qty;
    total += sub;
    // Determine path depth dynamically based on where we are, but handle absolute URLs correctly
    let imgPath = item.image;
    if (window.location.pathname.includes('/navigation/') && !imgPath.startsWith('http') && !imgPath.startsWith('data:')) {
      imgPath = '../' + imgPath.replace('../', '');
    }
    
    html += `
      <div class="cart-dropdown-item">
        <img src="${imgPath}" alt="${item.name}">
        <div class="cart-dropdown-info">
          <div class="cart-dropdown-title">${item.name}</div>
          <div class="cart-dropdown-price">${item.qty} x Qr ${item.price.toFixed(2)}</div>
        </div>
        <button class="cart-dropdown-remove" onclick="removeFromCart(${item.id})">&times;</button>
      </div>
    `;
  });
  html += '</div>';
  
  // Calculate relative URL to cart page correctly depending on current directory
  let cartUrl = window.location.pathname.includes('/navigation/') ? 'cart.html' : 'navigation/cart.html';
  
  html += `
    <div class="cart-dropdown-footer">
      <div class="cart-dropdown-total">
        <span>Total:</span>
        <span>Qr ${total.toFixed(2)}</span>
      </div>
      <a href="${cartUrl}" class="btn btn-primary" style="display:block; text-align:center; padding:8px 0; margin-top:10px;">View Cart</a>
    </div>
  `;
  dropdown.innerHTML = html;
}

