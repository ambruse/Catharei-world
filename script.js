window.setLanguage = function(langCode) {
  if(langCode === currentLang) return;
  currentLang = langCode;
  localStorage.setItem('catharei-lang', langCode);
  
  initLanguage();
  applyTranslations();
  
  const navList = document.getElementById('nav-list');
  if(navList) navList.classList.remove('active');
};

function applyTranslations() {
  const dictionary = translations[currentLang];
  
  const elements = document.querySelectorAll('[data-i18n]');
  elements.forEach(el => {
    const key = el.getAttribute('data-i18n');
    if(dictionary[key]) {
      if(el.tagName === 'SPAN' && el.parentElement.tagName === 'BUTTON') {
         el.textContent = dictionary[key];
      } else {
         el.textContent = dictionary[key];
      }
    }
  });

  const inputs = document.querySelectorAll('[data-i18n-ph]');
  inputs.forEach(input => {
    const key = input.getAttribute('data-i18n-ph');
    if(dictionary[key]) {
      input.placeholder = dictionary[key];
    }
  });
}


// --- Cake Scroll Animation ---
function initCakeAnimation() {
  const canvas = document.getElementById('cake-canvas');
  const section = document.getElementById('cake-scroll-section');
  const textOverlay = document.getElementById('cake-scroll-text');
  
  if (!canvas || !section) return;

  const context = canvas.getContext('2d');
  
  const frameCount = 240;
  // Compute image path depending on depth
  const isNav = window.location.pathname.includes('/navigation/');
  const prefix = isNav ? '../images/herosection_1/' : 'images/herosection_1/';
  
  const currentFrame = index => {
    // ezgif-frame-001.png
    const paddedIndex = index.toString().padStart(3, '0');
    return `${prefix}ezgif-frame-${paddedIndex}.png`;
  };

  const images = [];
  let loadedImages = 0;
  
  const preloadImages = () => {
    for (let i = 1; i <= frameCount; i++) {
      images[i] = new Image();
      images[i].onerror = () => { console.error("Failed to load frame", i, currentFrame(i)) };
      images[i].src = currentFrame(i);
      images[i].onload = () => {
        loadedImages++;
        if(i === 1) {
          updateImage(1);
        }
      };
    }
  };

  const updateImage = index => {
    if(!images[index]) return;
    
    // Fit image to canvas maintaining aspect ratio
    const img = images[index];
    if (!img.complete || img.naturalWidth === 0) return;

    const hRatio = canvas.width / img.width;
    const vRatio = canvas.height / img.height;
    const ratio = Math.min(hRatio, vRatio);
    const centerShift_x = (canvas.width - img.width * ratio) / 2;
    const centerShift_y = (canvas.height - img.height * ratio) / 2;  

    context.clearRect(0, 0, canvas.width, canvas.height);
    context.drawImage(img, 0, 0, img.width, img.height,
                      centerShift_x, centerShift_y, img.width * ratio, img.height * ratio);
  };

  const setCanvasSize = () => {
    canvas.width = window.innerWidth;
    canvas.height = window.innerHeight - 80; // minus header height
    // After resizing, update image dynamically if we have scroll data
    const scrollTop = html.scrollTop;
    let fallbackFrame = 1;
    if(section.offsetTop > 0) {
      const sectionScroll = scrollTop - section.offsetTop;
      let scrollFraction = sectionScroll / (section.offsetHeight - window.innerHeight);
      if (scrollFraction < 0) scrollFraction = 0;
      if (scrollFraction > 1) scrollFraction = 1;
      fallbackFrame = Math.min(frameCount, Math.floor(scrollFraction * frameCount) + 1);
    }
    // Only update if image is available, otherwise it will draw on load
    if (images[fallbackFrame] && images[fallbackFrame].complete) {
      updateImage(fallbackFrame);
    }
  };

  const html = document.documentElement;

  setCanvasSize();
  window.addEventListener('resize', setCanvasSize);

  preloadImages();

  window.addEventListener('scroll', () => {
    const scrollTop = html.scrollTop;
    
    // Check if section is in view
    const sectionTop = section.offsetTop;
    const sectionHeight = section.offsetHeight;
    const sectionBottom = sectionTop + sectionHeight;
    
    if (scrollTop + window.innerHeight < sectionTop || scrollTop > sectionBottom) {
      return; // out of view
    }

    const maxScrollObj = sectionHeight - window.innerHeight;
    const scrollFraction = (scrollTop - sectionTop) / maxScrollObj;
    
    let frameIndex = Math.floor(scrollFraction * frameCount) + 1;
    if (frameIndex < 1) frameIndex = 1;
    if (frameIndex > frameCount) frameIndex = frameCount;

    requestAnimationFrame(() => updateImage(frameIndex));
    
    // Evaluate overlay text fading. Fade in between 20% and 80% scroll
    if(textOverlay) {
      if(scrollFraction > 0.1 && scrollFraction < 0.9) {
        textOverlay.style.opacity = 1;
      } else {
        textOverlay.style.opacity = 0;
      }
    }
  });
}
let currentUser = null;

async function checkLoginStatus() {
  try {
    const res = await fetch('/api/auth/me');
    const data = await res.json();
    if (data.loggedIn) {
      currentUser = data.user;
    }
  } catch (err) {
    console.error("Error checking login status:", err);
  }
}

function handleAccountClick() {
  const isSubPage = window.location.pathname.includes('/navigation/');
  const root = isSubPage ? '../' : '';
  
  if (currentUser) {
    if (currentUser.role === 'admin') {
      window.location.href = root + 'admin.html';
    } else {
      window.location.href = root + 'account.html';
    }
  } else {
    window.location.href = root + 'login.html';
  }
}
